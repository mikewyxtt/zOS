use hal::boot::bootinfo::BootInfo;
use core::fmt::{Write, Error, self};


const FONT_WIDTH: u32 = 8;
const FONT_HEIGHT: u32 = 16;
const TAB_WIDTH: u32 = 4;

/* just keep a txt buffer and refresh the screen after each write. this way messages before the fb are up arent lost, and it will prob look better */
// I dont think we need the buffer in the writer struct....

/// Prints a formatted string with NO trailing newline to console output.
#[macro_export]
macro_rules! early_log_plain {
    ($bootinfo:expr, $($arg:tt)*) => { 
        ($crate::early_log::_early_log($bootinfo, format_args!("[ {} ]: ", env!("LOG_DISPLAY_NAME"))));
        ($crate::early_log::_early_log($bootinfo, format_args!($($arg)*)));
    }
}


/// Prints a formatted string with a trailing newline to console output.
#[macro_export]
macro_rules! early_log {
    ($bootinfo:expr) => ($crate::serial_log_plain!($bootinfo, "\n"));
    ($bootinfo:expr, $($arg:tt)*) => ($crate::early_log_plain!($bootinfo, "{}\n", format_args!($($arg)*)));
}


/// Early logging function, used by early_log! macro. Creates a temporary text buffer and writes the formatted text to it.
/// The text is then immediately output to the serial port, then the buffer is discarded.
#[doc(hidden)]
pub fn _early_log(bootinfo: &mut BootInfo, args: fmt::Arguments) {
    let mut early_log_writer = EarlyLogWriter::new(bootinfo);
    early_log_writer.write_fmt(args).unwrap();
}


/// Writer struct for fmt::Write to use. We don't need to do anything with it other than use it as a place to process the formatted text into, it is simple.
pub struct EarlyLogWriter<'a> {
    buffer: [u8; 100],
    position: usize,
    bootinfo: &'a mut BootInfo,
}

impl<'a> EarlyLogWriter<'a> {

    /// Returns an empty SerialWriter struct
    fn new(_bootinfo: &'a mut BootInfo) -> Self {
        EarlyLogWriter {
            buffer: [b'\0'; 100],
            position: 0,
            bootinfo: _bootinfo
        }
    }

    /// Writes a single byte to framebuffer and serial port if it's enabled
    pub fn write_byte(&mut self, byte: u8) {
        
        // Write char to serial port if it's enabled
        if self.bootinfo.serial.enabled {
            // Add carriage return if it's a newline, serial is weird
            if byte == b'\n' {
                unsafe { hal::io::write_byte(self.bootinfo.serial.port, b'\r' as u8); }
            }

            unsafe { hal::io::write_byte(self.bootinfo.serial.port, byte); }
        }

        if self.bootinfo.framebuffer.enabled {
            console_put_char(self.bootinfo, byte as char);
        }

        self.position += 1;
    }
}

/// In order to use the formatting stuff from the 'core' lib, we must provide the write_str implementation.
impl<'a> fmt::Write for EarlyLogWriter<'a> {
    fn write_str(&mut self, s: &str) -> Result<(), Error> {
        for byte in s.bytes() {
            self.write_byte(byte);
        }
        Ok(())
    }
}


/// Plots a single pixel to the framebuffer
fn fb_plot_pixel(bootinfo: &BootInfo, x: u32, y: u32) {
    // RGB HEX value for white
    const FB_COLOR_WHITE: u32 = 0xFFFFFF;

    if bootinfo.framebuffer.depth != 4 {
        panic!("Frambuffer depth is not 4 bytes. Unsupported.");
    }

    // Don't go beyond framebuffer boundary
    if x < bootinfo.framebuffer.width && y < bootinfo.framebuffer.height {
        let offset = (y * bootinfo.framebuffer.width * bootinfo.framebuffer.depth) + (x * bootinfo.framebuffer.depth);

        // Plot a white pixel to the framebuffer by writing the color code 
        unsafe { *((bootinfo.framebuffer.addr + offset as usize) as *mut u32) = FB_COLOR_WHITE }
    }
}


/// Prints a single char to the framebuffer
fn console_put_char(bootinfo: &mut BootInfo, c: char) {

    // If it's a newline or tab, just move the cursor without rendering a font.
    match c {
        //If it's a tab, index the cursor 4 spaces, unless we don't have room. In which case we newline.
        '\t' => {
            if bootinfo.console.cursor_pos < bootinfo.console.max_chars - TAB_WIDTH {
                bootinfo.console.cursor_pos += TAB_WIDTH;
                return;
            } else {
                // Jump to newline handling
                console_newline(bootinfo);
                return;
            }
        }
        '\n' => {
            // Jump to newline handling
            console_newline(bootinfo);
            return;
        }
        _ => {}
    }

    // Draw the char
    console_draw_char(bootinfo, c);
    

    // Index cursor forward
    if bootinfo.console.cursor_pos < bootinfo.console.max_chars - 1 {
        bootinfo.console.cursor_pos += 1;
    } else {
        console_newline(bootinfo);
        return;
    }
}


/// Draws a char on the framebuffer
fn console_draw_char(bootinfo: &mut BootInfo, c: char) {
    /*
     * The early log framebuffer console is very simple. It uses bitmap fonts which essentially sets a bit for each pixel in the font. The fonts are 8 pixels wide and 16 pixels tall.
     * The array is easy to use, the first index is the ASCII character code, the second represents the row of pixels within the font. There are 16 rows.
     * 
     *
     * Example for the letter 'A':
     * Console_Font['A'][0] =  0b00000000;
     * Console_Font['A'][1] =  0b00000000;
     * Console_Font['A'][2] =  0b00000000;
     * Console_Font['A'][3] =  0b00010000;
     * Console_Font['A'][4] =  0b00111000;
     * Console_Font['A'][5] =  0b01101100;
     * Console_Font['A'][6] =  0b11000110;
     * Console_Font['A'][7] =  0b11000110;
     * Console_Font['A'][8] =  0b11111110;
     * Console_Font['A'][9] =  0b11000110;
     * Console_Font['A'][10] = 0b11000110;
     * Console_Font['A'][11] = 0b11000110;
     * Console_Font['A'][12] = 0b11000110;
     * Console_Font['A'][13] = 0b00000000;
     * Console_Font['A'][14] = 0b00000000;
     * Console_Font['A'][15] = 0b00000000;
     * 
     * To print the char we index to the ascii code offset of the array and iterate through each bit of the bitmap. We plot a pixel if the bit is set.
     */

     let mut x = (bootinfo.console.cursor_pos * FONT_WIDTH) as u32; // Starting x position
     let mut y = (bootinfo.console.line * FONT_HEIGHT) as u32; // Starting y position
 
     // Iterate through each row(byte) of the bitmap font
     for row in CONSOLE_FONT[c as usize].iter() {
 
         // Iterate through each bit(column) of the bitmap font
         for bit in (0..FONT_WIDTH).rev() {
 
             // Check if the bit at the current position is set in the font character
             if (row >> bit) & 1 != 0 { 
                 fb_plot_pixel(bootinfo, x, y);
             }
             x += 1;
         }
         y += 1; // Move to the next row
         x -= FONT_WIDTH; // Reset x back to the start of the row
     }
}

fn console_newline(bootinfo: &mut BootInfo) {
    // Check if we are at the last console line. If we aren't, increment it by one line.
    // Otherwise, we need to scroll by one line.
    if bootinfo.console.line < bootinfo.console.max_line - 1 {
        bootinfo.console.line += 1;
        bootinfo.console.cursor_pos = 0;
    } else {
        // Call a function to handle scrolling
        bootinfo.console.cursor_pos = 0;
        console_scroll(bootinfo);
    }
}


fn console_scroll(bootinfo: &mut BootInfo) {
    /* 
     * To scroll the console we first copy every pixel below the top line to the beginning of the framebuffer. Then we clear out the bottom line since it still contains what used to be the
     * bottom most row of text.
     *
     * The calculations are simple:
     * The size of one row of text (in bytes) is equivalent to the height of the font * the pitch (number of bytes needed to index one pixel down)
     *
     * To skip the first row of text, we simply add that number to the base address of the framebuffer.
     *
     * To find out how many bytes we need to copy, we simply subtract the size of one row of text (in bytes) from the total size of the framebuffer (in bytes).
     * 
     * To clear the bottom row of text, we find the beginning of the bottom row bu subtracting the size of one row of text from the total size of the framebuffer, then we set the size of one
     * row of text (in bytes) to 0, which turns all of those pixels off.
     */

    let base = bootinfo.framebuffer.addr as *mut u8;
    let row_size = bootinfo.framebuffer.pitch as usize * FONT_HEIGHT as usize;

    // Shift screen contents up one row
    let src = (base as usize + row_size) as *mut u8;
    let count = bootinfo.framebuffer.size as usize - row_size;
    unsafe { core::ptr::copy(src, base, count); }
    

    // Clear the bottom row
    let offset = (base as usize + bootinfo.framebuffer.size as usize - row_size) as *mut u8;
    unsafe { core::ptr::write_bytes(offset, 0, row_size); }
}


